def topics_summary_for(date: str) -> str:
    """index 用：その日のトピック（テーマ名）をカンマ区切りで。"""
    integ = find_integration(date)
    themes: List[str] = []
    if integ is not None:
        _h1, sections, _ = split_h1_sections(integ.read_text(encoding="utf-8"))
        for head, body in sections:
            if "トピック" in head:
                for _key, theme, _link in parse_topic_table(body):
                    if theme:
                        themes.append(theme)
                break
    if not themes:
        for key in DOMAIN_KEYS:
            e = find_domain_entry(key, date)
            if e:
                t, _tags, _b = parse_domain_entry(e)
                themes.append(t)
    return "、".join(themes)


