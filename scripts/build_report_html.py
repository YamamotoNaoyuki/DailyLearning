def today_str() -> str:
    try:
        out = subprocess.run(["date", "+%Y-%m-%d"], capture_output=True, text=True, check=True)
        s = out.stdout.strip()
        if DATE_RE.fullmatch(s):
            return s
    except Exception:
        pass
    return _dt.date.today().isoformat()


def write_if_changed(path: Path, content: str) -> bool:
    path.parent.mkdir(parents=True, exist_ok=True)
    if path.is_file() and path.read_text(encoding="utf-8") == content:
        return False
    path.write_text(content, encoding="utf-8")
    return True


def main(argv: List[str]) -> int:
    args = [a for a in argv[1:] if a]
    backfill_all = "--all" in args
    date_args = [a for a in args if not a.startswith("-")]

    if backfill_all:
        target_dates = all_integration_dates()
        if not target_dates:
            sys.exit("エラー: _integrations/ にレポートが見つかりません。")
    else:
        date = date_args[0] if date_args else today_str()
        if not DATE_RE.fullmatch(date):
            sys.exit(f"エラー: 日付は YYYY-MM-DD 形式で指定してください（受け取った値: {date!r}）。")
        target_dates = [date]

    # 生成対象 = 指定日 ∪ 既に docs/reports/ にある日（ナビ整合のため再生成）
    report_dates = sorted(set(existing_report_dates()) | set(target_dates))

    DOCS_DIR.mkdir(parents=True, exist_ok=True)
    REPORTS_DIR.mkdir(parents=True, exist_ok=True)

    written = []
    final_dates = []
    for d in report_dates:
        doc = build_report_html(d, report_dates)
        if doc is None:
            # 統合レポートが無い → 既存 .html があればそのまま残す
            if (REPORTS_DIR / f"{d}.html").is_file():
                final_dates.append(d)
            continue
        if write_if_changed(REPORTS_DIR / f"{d}.html", doc):
            written.append(f"docs/reports/{d}.html")
        final_dates.append(d)

    # ナビが report_dates 前提で生成されているので、欠けがあった場合に備えてもう一周（軽量）
    if set(final_dates) != set(report_dates):
        for d in final_dates:
            doc = build_report_html(d, final_dates)
            if doc and write_if_changed(REPORTS_DIR / f"{d}.html", doc):
                if f"docs/reports/{d}.html" not in written:
                    written.append(f"docs/reports/{d}.html")

    if write_if_changed(DOCS_DIR / "index.html", build_index_html(final_dates)):
        written.append("docs/index.html")

    if written:
        print("生成/更新:")
        for w in written:
            print(f"  - {w}")
    else:
        print("変更なし（生成済みと同一）。")
    print(f"レポート総数: {len(final_dates)}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main(sys.argv))
