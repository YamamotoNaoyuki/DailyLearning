#!/usr/bin/env python3
"""
build_report_html.py — 日次学習レポートを「自己完結 HTML」に変換する。

使い方:
    python3 scripts/build_report_html.py [YYYY-MM-DD]   # 指定日（省略時はローカル今日）
    python3 scripts/build_report_html.py --all          # 既存の全レポートを再生成（バックフィル）

挙動:
- `_integrations/<date>_*.md`（統合レポート）と `domains/<分野>/<date>_*.md`（各ドメイン全文）
  を読み込み、`docs/reports/<date>.html` を生成する。
- 各ドメインの「今日のテーマ概要」カード自体が <details>（折りたたみ）で、クリックすると
  そのドメインの全文 markdown がレンダリングされて開く。ローカルの .md は一切変更しない。
- CSS は `docs/style.css` をインライン展開するので、生成 HTML は単体でも開ける／共有できる。
- `docs/index.html` を再生成し、`docs/reports/` に既に存在するレポートのナビ（前日/翌日）も
  整合するよう貼り直す（過去 .md の一括 HTML 化はしない＝既存 .html があるものだけ）。

依存: 標準ライブラリ + `markdown`（`pip install markdown` / 既存環境では導入済み）。
"""
